## Что произошло

В ходе подготовки отдельной исследовательской Сущности по репозиторию teraOrigin стало ясно, что простой передачи набора файлов и стартового промпта недостаточно.

На практике были выявлены следующие трудности:

- оператору неудобно вручную выбирать большой набор файлов в корне каталога;
- переключение между окнами чата, проводника и инструкций на планшете приводит к сбросу положения и росту вероятности ошибок;
- новая Сущность может формально понять задачу, но не удерживать дисциплину шага;
- новая Сущность может принять shell-конвейер выдачи файлов, но при этом самовольно перескакивать к “умным” интеграционным документам вместо точного исполнения нулевого теста;
- для переноса хорошо работающего конвейера из одного чата в другой потребовалось вводить отдельный операторский контур запуска.

## Какие выводы сделаны

Сделаны следующие практические выводы:

1. Для запуска новых специализированных Сущностей требуется не просто стартовый пакет, а многоуровневая операторская структура.
2. Нужно разделять:
   - минимальный запуск и проверку конвейера;
   - базовый технический контекст;
   - миссионный и архитектурный контекст;
   - рабочие шаблоны исследовательской фазы;
   - служебные и архивные материалы.
3. Нулевой допуск должен проверять не только формат shell-выдачи, но и послушание шагу.
4. Новая Сущность должна получать допуск к каждой следующей подфазе отдельно.
5. Для устойчивой работы требуется фигура координирующей Сущности, а не только Архивариус и набор дочерних рабочих чатов.

