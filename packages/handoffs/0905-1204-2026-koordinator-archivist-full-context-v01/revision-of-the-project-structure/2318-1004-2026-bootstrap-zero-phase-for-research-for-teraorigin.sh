Назначение: зафиксировать обязательные статусы, которыми должна маркироваться любая сильная техническая трактовка по репозиторию `teraOrigin`

## 1. Главное правило

Нельзя писать сильные выводы без указания статуса доказательства.

## 2. Допустимые статусы

### 2.1. Обнаружено в исходниках
Использовать, когда свойство прямо найдено в коде, конфиге, структуре проекта или документации репозитория.

### 2.2. Подтверждено запуском
Использовать, когда свойство реально наблюдалось при поднятии ноды.

### 2.3. Подтверждено экспериментом
Использовать, когда свойство было специально проверено тестом, серией действий или контролируемой нагрузкой.

### 2.4. Наблюдалось в логах
Использовать, когда вывод сделан из логов, журналов, сообщений консоли или иных следов выполнения.

### 2.5. Обоснованная гипотеза
Использовать, когда вывод пока не подтверждён практикой, но хорошо поддержан чтением кода и структурой проекта.

### 2.6. Требует проверки
Использовать, когда участок важен, но пока недостаточно данных даже для аккуратной гипотезы.

## 3. Запрещённые практики

Нельзя:
- путать гипотезу с фактом;
- путать найденное в коде с проверенным запуском;
- делать широкие архитектурные выводы из одного файла без сверки;
- изображать уверенность там, где нет проверки.

